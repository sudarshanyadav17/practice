for(let i=1;i<=10;i++)
{
    console.log("The For Say :",i);
}
console.log("---Print in Desending Order");

for(let i=10;i>=1;i--)
{
    console.log("The For Say :",i);
}

console.log("---Print Only Even Number---");

for(let i=2;i<=10;i=i+2)
{
    console.log("The For Say :",i);
}

console.log("---Print Only Odd Number---");

for(let i=1;i<=10;i=i+2)
{
    console.log("The For Say :",i);
}


